def get_subcategories(path):
    """
    Повертає підкатегорії за шляхом (список вибраних категорій).
    :param path: list[str] - шлях до категорії
    :return: dict або None
    """
    node = services
    for key in path:
        if key in node:
            node = node[key]
        else:
            return None
    return node if isinstance(node, dict) else None
